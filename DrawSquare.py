import turtle

def draw_square ():
    window = turtle.screen()
    window.bgcolor("red")

    P = turtle.Turtle()
    P.color ("yellow")
    P.shape ("turtle")
    P.speed (2)
    P.forward (100)
    P.right(90)
    P.forward (100)
    P.right(90)
    P.forward (100)
    P.right(90)
    P.forward (100)
    P.right(90)
